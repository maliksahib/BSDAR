    public void setBadSmellAnalyticsResultPanel() {
        if (DetectionFlag.isLongMethodAnalyticsFlag()) {
            // JOptionPane.showMessageDialog(rootPane, Metrics.getNumberOfMethods()+"@@"+BadSmells.getLongMethodBadSmells().size());
            bsaResultPanel.setPanelForLongMethod(Metrics.getNumberOfMethods(), BadSmells.getLongMethodBadSmells().size());
            DetectionFlag.setLongMethodFlag(false);
        } else if (DetectionFlag.isLongParameterListAnalyticsFlag()) {
            bsaResultPanel.setPanelForLongParameterList(Metrics.getNumberOfMethods(), BadSmells.getLongParameterListBadSmells().size());
            DetectionFlag.setLongParameterListFlag(false);
        }
        setBadSmellAnalyticsResultPanelVisible();
    }

