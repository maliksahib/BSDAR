    private boolean isFileChooserSelectedProjectOk(JFileChooser fileChooser) {
        Project.setProjectPath(fileChooser.getSelectedFile().toString());
        Project.setProjectName(fileChooser.getSelectedFile().getName());

        File file = new File(Project.getProjectPath());
        File[] files = file.listFiles();
        boolean flag = false;
        for (File f : files) {
            if (f.getName().equals("src")) {
                Project.setProjectSourceFolderPath(f.getPath().toString());
                flag = true;
                break;
            }
        }
        return flag;
    }

