    private boolean projectContainFiles(File file) {
        projectFileFilter(file);
        if (projectJavaFilesPath.isEmpty()) {
            return false;
        } else {
            return true;
        }
    }

    private void projectFileFilter(File directory) {

        File[] filePath = directory.listFiles();
        for (File f : filePath) {
            if (f.isDirectory()) {
                checkJavaPackage(f);
                projectFileFilter(f);
            } else if (f.getName().toString().endsWith(".java")) {
                projectJavaFilesPath.add(f);
            }
        }
    }

    private void checkJavaPackage(File file) {
        File[] files = file.listFiles();
        for (File f : files) {
            if (f.getName().toString().endsWith(".java")) {
                projectJavaPackagesPath.add(file);
                break;
            }
        }

    }

