    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        setProjectSelectionPanelEmpty();
        Metrics.setProjectMetricsEmpty();
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select Your Java Project!");
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);

        if (fileChooser.showOpenDialog(jButton1) == JFileChooser.APPROVE_OPTION) {
            if (this.isFileChooserSelectedProjectOk(fileChooser)) {
                jTextField1.setText(Project.getProjectPath());
                if (projectContainFiles(new File(Project.getProjectSourceFolderPath()))) {
                    jLabel3.setText(Project.getProjectName());
                } else {
                    jLabel3.setText(Project.getProjectName() + "  " + "But it does not contain any Java file.");
                }
                jPanel4.setVisible(true);
                jPanel5.setVisible(false);
                Project.setProjectJavaFilesPath(projectJavaFilesPath);
                Project.setProjectJavaPackagesPath(projectJavaPackagesPath);
                projectJavaFilesPath = new ArrayList<File>();
                projectJavaPackagesPath = new ArrayList<File>();
            } else {
                whenFileChooserNotSelectRightProject();
            }

        } else {
            whenFileChooserNotSelectRightProject();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

